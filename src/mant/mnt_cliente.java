    @Override
    public boolean check() {
        if(isEmpy(txtDir) || isEmpy(txtTel) || isEmpy(txtNombre) 
                || isEmpy(txtCorreo) )
            return false;
        else
            return true;
    }

    @Override
    public void clean() {
        String txt= "";
        txtNombre.setText(txt);
        txtDir.setText(txt);
        txtCorreo.setText(txt);
        txtTel.setText(txt);
        
        btnGrabar.setEnabled(true);
        btnEliminar.setEnabled(false);
        btnMod.setEnabled(false);
        btnSeleccionar.setEnabled(false);
    }

    @Override
    public void fill_table(ActionEvent evt) {
       DefaultTableModel modelo = new DefaultTableModel();
        String [] cols = {
                "ID", "NAME", "TELEFONO", "DIRECCION", "CORREO"};
        
        for (int i=0;i<cols.length;i++)
            modelo.addColumn(cols[i]);
        
        ArrayList<Cliente> list = null;
        try { 
            list = Cliente.select();
        } catch (SQLException ex) {
            Mensajes.mensajeError(evt, "DB");
            Logger.getLogger(mnt_empleado.class.getName()).
                    log(Level.SEVERE, null, ex);
        }
        
        int k;
            for(Cliente a : list){
                k=0;
                Object[] fila = new Object[5];
                fila[k++]=(Object)a.getId();
                fila[k++]=(Object)a.getName();
                fila[k++]=(Object)a.getTelefono();
                fila[k++]=(Object)a.getAddress();
                fila[k++]=(Object)a.getEmail();
                modelo.addRow(fila);
            }
            tabCli.setModel(modelo);
    }

    @Override
    public void llenar_campos(Object e) {
        if(e instanceof Cliente){
            Cliente c = (Cliente)e;
            txtNombre.setText(c.getName());
            txtTel.setText(c.getTelefono());
            txtDir.setText(c.getAddress());
            txtCorreo.setText(c.getEmail());
            
            btnEliminar.setEnabled(true);
            btnMod.setEnabled(true);
            btnGrabar.setEnabled(false);
        }
    }

