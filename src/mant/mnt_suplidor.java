    private void btnModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModActionPerformed
        int descision = Mensajes.confirmar(evt,"DESEA ELIMINAR EL EMPLEADO");
        if(descision !=0){
            return;
        }

        Suplidor s = new Suplidor();
        s.setId(selected_suplidor.getId());
        s.setNombre(txtNombre.getText());
        s.setTelefono(txtTel.getText());
        s.setDireccion(txtDirec.getText());
       
        
        try {
            selected_suplidor.update(s);
            clean();
        } catch (SQLException ex) {
            Logger.getLogger(mnt_empleado.class.getName()).
            log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnModActionPerformed

