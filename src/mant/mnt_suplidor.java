    private boolean isEmpy(JTextComponent txt){
        if(txt.getText().equals(""))
            return true;
        else
            return false;
    }
    
    @Override
    public boolean check() {
        if(isEmpy(txtNombre) || isEmpy(txtTel) || isEmpy(txtDirec))
            return false;
        else
            return true;
    }

    @Override
    public void clean() {
        String txt = "";
        txtNombre.setText(txt);
        txtTel.setText(txt);
        txtDirec.setText(txt);
        
        btnGrabar.setEnabled(true);
        btnSeleccionar.setEnabled(false);
    }

    @Override
    public void fill_table(ActionEvent evt) {
        DefaultTableModel modelo = new DefaultTableModel();
        String [] cols = {
                "ID", "NOMBRE", "TELEFONO", "DIRECCION"};
        for (int i=0;i<cols.length;i++)
            modelo.addColumn(cols[i]);
        
        ArrayList<Suplidor> list = null;
        try { 
            list = Suplidor.select();
        } catch (SQLException ex) {
            Logger.getLogger(mnt_empleado.class.getName()).
                    log(Level.SEVERE, null, ex);
            Mensajes.mensajeError(evt, "ERROR LLENANDO LA TABLA ");
        }
        
        int k;
            for(Suplidor s : list){
                k=0;
                Object[] fila = new Object[4];
                fila[k++]=(Object)s.getId();
                fila[k++]=(Object)s.getNombre();
                fila[k++]=(Object)s.getTelefono();
                fila[k++]=(Object)s.getDireccion();
                
                modelo.addRow(fila);
            }
            tabSupl.setModel(modelo);
    
    }

    @Override
    public void llenar_campos(Object e) {
        if(e instanceof Suplidor){
            Suplidor s = (Suplidor)e;
            txtNombre.setText(s.getNombre());
            txtTel.setText(s.getTelefono());
            txtDirec.setText(s.getDireccion());
            
            btnSeleccionar.setEnabled(true);
            btnEliminar.setEnabled(true);
            btnMod.setEnabled(true);
        }
    }
