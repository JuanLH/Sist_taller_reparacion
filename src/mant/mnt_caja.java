    private void btnModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModActionPerformed
        int descision = Mensajes.confirmar(evt,"DESEA MODIFICAR LA CAJA");
        
        if(descision !=0){
            return;
        }
        
        if(check() ){
            Caja c = new Caja();
            c.setId(selected_caja.getId());
            c.setId_empleado(txtCedula.getText());
            c.setFondo(Integer.parseInt(txtFondo.getText()));
            try {
                c.update(c);
                clean();
            } catch (SQLException ex) {
                Mensajes.mensajeError(evt, "DB");
                Logger.getLogger(mnt_empleado.class.getName()).
                log(Level.SEVERE, null, ex);
            }
        }
        else{
            Mensajes.mensajeError(evt, "NO DEJE CAMPOS EN BLANCO");
        }
        fill_table(evt);

    }//GEN-LAST:event_btnModActionPerformed

