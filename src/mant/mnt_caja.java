    @Override
    public boolean check() {
        if(isEmpy(txtCedula) || isEmpy(txtFondo) || isEmpy(txtNombreEmpleado)){
            return false;
        }
        return true;
    }

    @Override
    public void clean() {
        String txt = "";
        txtCedula.setText(txt);
        txtNombreEmpleado.setText(txt);
        txtFondo.setText(txt);
        
        btnGrabar.setEnabled(true);
        btnMod.setEnabled(false);
        btnEliminar.setEnabled(false);
    }

    @Override
    public void fill_table(ActionEvent evt) {
        DefaultTableModel modelo = new DefaultTableModel();
        String [] cols = {
                "ID", "ID_EMPLEADO", "FONDO", "STATUS"};
        for (int i=0;i<cols.length;i++)
            modelo.addColumn(cols[i]);
        
        ArrayList<Caja> list = null;
        try { 
            list = Caja.select();
        } catch (SQLException ex) {
            Mensajes.mensajeError(evt, "DB");
            Logger.getLogger(mnt_empleado.class.getName()).
                    log(Level.SEVERE, null, ex);
            Mensajes.mensajeError(evt, "ERROR DE BASE DE DATOS ");
        }
        
        int k;
            for(Caja us : list){
                k=0;
                Object[] fila = new Object[4];
                fila[k++]=(Object)us.getId();
                fila[k++]=(Object)us.getId_empleado();
                fila[k++]=(Object)us.getFondo();
                fila[k++]=(Object)us.getStatus();
                
                modelo.addRow(fila);
            }
            tabCaja.setModel(modelo);
    }

    @Override
    public void llenar_campos(Object e) {
        if(e instanceof Caja){
            Caja c= (Caja)e;
            txtCedula.setText(c.getId_empleado());
            try {
                txtNombreEmpleado.setText(
                        (String)Empleados.get(c.getId_empleado(), "name"));
            } catch (SQLException ex) {
                Logger.getLogger(mnt_caja.class.getName())
                        .log(Level.SEVERE, null, ex);
            }
            txtFondo.setText(Integer.toString(c.getFondo()));
            cmbStatus.setSelectedIndex(c.getStatus());
        }
    }
